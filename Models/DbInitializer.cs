using System;
using System.Collections.Generic;
using System.Linq;
using TgPredictBot.Models;

namespace TgPredictBot
{
    public static class DbInitializer
    {
        public static void Seed(AppDbContext context)
        {
            if (!context.TarotCards.Any())
            {
                var allCards = new List<TarotCard>();

                // 1. Ручное наполнение Старших Арканов (заложим основные)
                var majorArcanaData = new Dictionary<TarotCardType, (string Name, string Desc)>
                {
                    { TarotCardType.Fool, ("Дурак (The Fool)", "Время для новых начинаний и чистой веры в лучшее. Не бойся сделать шаг в неизведанное, вселенная тебя поддержит.") },
                    { TarotCardType.Magician, ("Маг (The Magician)", "Сегодня день проявления твоей воли. У тебя есть абсолютно все ресурсы, чтобы решить старую проблему. Действуй смело!") },
                    { TarotCardType.HighPriestess, ("Верховная Жрица (The High Priestess)", "Доверяй своей интуиции и подсознанию. Не все тайны нужно раскрывать прямо сейчас, умей выжидать.") },
                    { TarotCardType.Empress, ("Императрица (The Empress)", "День плодородия, творчества и изобилия. Прекрасное время для заботы о себе, близких и созидания нового.") },
                    { TarotCardType.Emperor, ("Император (The Emperor)", "Время навести порядок, проявить дисциплину и структуру. Твоя сила — в стабильности и логике.") },
                    { TarotCardType.WheelOfFortune, ("Колесо Фортуны (The Wheel of Fortune)", "Судьба совершает неожиданный поворот. Ситуация меняется в твою пользу, поймай эту волну!") },
                    { TarotCardType.Death, ("Смерть (The Death)", "Не бойся этого имени. Это карта глобальной трансформации. Старое отмирает, освобождая место для чего-то прекрасного.") },
                    { TarotCardType.Sun, ("Солнце (The Sun)", "День абсолютного успеха, ясности и жизненной энергии. Твои труды будут замечены. Наслаждайся моментом!") },
                    { TarotCardType.World, ("Мир (The World)", "Цикл завершен, гармония достигнута. Ты на своем месте и заслуживаешь триумфа. Всё сложится наилучшим образом.") }
                };

                // Добавляем заполненные Старшие Арканы
                foreach (var item in majorArcanaData)
                {
                    allCards.Add(new TarotCard { CardType = item.Key, Name = item.Value.Name, Description = item.Value.Desc });
                }

                // Дозаполняем остальные Старшие Арканы базовыми заглушками, если их нет в словаре выше
                foreach (TarotCardType type in Enum.GetValues(typeof(TarotCardType)))
                {
                    if ((int)type < 100 && !majorArcanaData.ContainsKey(type))
                    {
                        allCards.Add(new TarotCard
                        {
                            CardType = type,
                            Name = $"{type} (Старший Аркан)",
                            Description = "Этот глубокий Старший Аркан указывает на важные ментальные изменения. Вселенная советует прислушаться к внутреннему голосу."
                        });
                    }
                }

                // 2. АВТОМАТИЧЕСКАЯ ГЕНЕРАЦИЯ ДЛЯ 56 МЛАДШИХ АРКАНОВ
                string[] suits = { "Жезлов", "Кубков", "Мечей", "Пентаклей" };
                string[] values = { "Туз", "Двойка", "Тройка", "Четверка", "Пятерка", "Шестерка", "Семерка", "Восьмерка", "Девятка", "Десятка", "Паж", "Рыцарь", "Королева", "Король" };

                var suitDescriptions = new Dictionary<string, string>
                {
                    { "Жезлов", "Масть Жезлов отвечает за энергию, страсть, амбиции и карьеру. " },
                    { "Кубков", "Масть Кубков символизирует эмоции, любовь, интуицию и отношения. " },
                    { "Мечей", "Масть Мечей связана с разумом, логикой, испытаниями и преодолением трудностей. " },
                    { "Пентаклей", "Масть Пентаклей олицетворяет земной план: деньги, работу, здоровье и стабильность. " }
                };

                var valueDescriptions = new Dictionary<string, string>
                {
                    { "Туз", "Это мощный импульс, чистая энергия масти и великолепный шанс начать всё с чистого листа. Действуйте!" },
                    { "Двойка", "Карта указывает на выбор, балансирование, партнерство или необходимость найти компромисс." },
                    { "Тройка", "Символизирует первые результаты, расширение горизонтов или важную веху в развитии ситуации." },
                    { "Четверка", "Время стабильности, отдыха и укрепления позиций. Выстройте надежные границы вокруг себя." },
                    { "Пятерка", "Предупреждает о кризисе, конкуренции, временных потерях или внутреннем конфликте. Будьте бдительны." },
                    { "Шестерка", "Знаменует победу, гармонию, выход из тяжелой ситуации или заслуженное признание ваших заслуг." },
                    { "Семерка", "Карта выбора, оценки скрытых мотивов, защиты своих позиций или преодоления тайных страхов." },
                    { "Восьмерка", "Говорит о стремительном движении, трансформации, обучении или необходимости выйти из ограничений." },
                    { "Девятка", "Указывает на кульминацию процессов, обретение мудрости, внутреннюю силу или избавление от тревог." },
                    { "Десятка", "Символ завершения, максимального результата масти (как в абсолютном успехе, так и в сильной усталости)." },
                    { "Паж", "Вас ждут новости, свежие идеи или новые шансы. Будьте открыты миру как ученик." },
                    { "Рыцарь", "Период активных действий, движения, поездок или напористого продвижения своих интересов." },
                    { "Королева", "Проявите мягкую силу, эмпатию, внутреннюю уверенность и мудрость в решении сегодняшних вопросов." },
                    { "Король", "Время взять на себя полную ответственность, проявить авторитет, мастерство и стратегическое мышление." }
                };

                foreach (TarotCardType type in Enum.GetValues(typeof(TarotCardType)))
                {
                    int val = (int)type;
                    if (val >= 100) // Фильтруем только Младшие Арканы
                    {
                        int suitIndex = (val / 100) - 1; // 0 = Жезлы, 1 = Кубки, 2 = Мечи, 3 = Пентакли
                        int valueIndex = val % 100 - 1;  // 0 = Туз, 1 = Двойка ... 13 = Король

                        string suitName = suits[suitIndex];
                        string valueName = values[valueIndex];

                        string cardName = $"{valueName} {suitName}";
                        string cardDescription = suitDescriptions[suitName] + valueDescriptions[valueName];

                        allCards.Add(new TarotCard
                        {
                            CardType = type,
                            Name = cardName,
                            Description = cardDescription
                        });
                    }
                }

                context.TarotCards.AddRange(allCards);
            }

            // 3. Заполняем общие предсказания (DailyPredictions), если таблица пуста
            if (!context.DailyPredictions.Any())
            {
                context.DailyPredictions.AddRange(
                    new DailyPrediction { Category = "Общий", Text = "Звезды советуют сегодня сбавить обороты и уделить время планированию. Энергия дня пассивна." },
                    new DailyPrediction { Category = "Любовь", Text = "Прекрасный момент для откровенного разговора со второй половинкой. Романтика сегодня на высоте." },
                    new DailyPrediction { Category = "Деньги", Text = "Будьте аккуратны с импульсивными покупками. Финансовые потоки требуют стабильности и экономии." }
                );
            }

            context.SaveChanges();
        }
    }
}